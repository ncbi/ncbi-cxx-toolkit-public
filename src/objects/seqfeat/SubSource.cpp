bool CSubSource::IsDayValueOkForMonth(int day, int month, int year)
{
    bool rval = true;
    if (month < 1 || month > 12) {
        return false;
    }
    if (day < 1) {
        return false;
    }
    int dpm = sm_daysPerMonth [month - 1];

    if (day > dpm) {
        rval = false;
        if (month == 2 && day == 29) {
            // true only if this is a leap year
            if (! (year % 400)) {
                rval = true;
            } else if (!(year % 100)) {
                rval = false;
            } else if (!(year % 4)) {
                rval = true;
            }
        }
    }
    return rval;
}


