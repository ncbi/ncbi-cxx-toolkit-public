                return "proprotein";
            case CProt_ref::eProcessed_mature:
                return "mat_peptide";
            case CProt_ref::eProcessed_signal_peptide:
                return "sig_peptide";
            case CProt_ref::eProcessed_transit_peptide:
                return "transit_peptide";
            default:
