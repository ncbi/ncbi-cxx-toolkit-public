void CCit_pat::GetLabel(string* label) const
{
    string date;
    string* date_ptr = &date;
    if ( IsSetDate_issue() ) {
        GetDate_issue().GetDate(&date, true);
    }
    else if ( IsSetApp_date() ) {
        GetApp_date().GetDate(&date, true);
    }
    else {
        date_ptr = 0;
    }

    GetLabelContent(label, false, &GetAuthors(), 0, 0, 0, 0,
        &GetCountry(),
        IsSetNumber() ? &GetNumber() :
            (IsSetApp_number() ? &GetApp_number() : 0),
        0, date_ptr);
}


void CCit_pat::BasicCleanup(bool fix_initials)
{
	if (IsSetAuthors()) {
		SetAuthors().BasicCleanup(fix_initials);
	}
	if (IsSetApplicants()) {
		SetApplicants().BasicCleanup(fix_initials);
	}
	if (IsSetAssignees()) {
		SetAssignees().BasicCleanup(fix_initials);
	}
}

