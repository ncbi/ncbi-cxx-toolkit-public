void CCit_sub::GetLabel(string* label, bool unique) const
{
    string date;
    if ( IsSetDate() ) {
        GetDate().GetDate(&date, "%{%M-%D-%}%Y");
    }
    GetLabelContent(label, unique, &GetAuthors(),
        IsSetImp() ? &GetImp() : 0,
        0, 0, 0, 0, 0, 0, IsSetDate() ? &date : 0);
}


void CCit_sub::BasicCleanup(bool fix_initials)
{
    CRef<TAuthors> authors;
    if (IsSetAuthors()) {
        authors.Reset(&SetAuthors());
        authors->BasicCleanup(fix_initials);
    }

    if (IsSetImp()) {
        TImp& imp = SetImp();
        if (authors  &&  !authors->IsSetAffil()  &&  imp.IsSetPub()) {
            authors->SetAffil(imp.SetPub());
            imp.ResetPub();
        }
        if (!IsSetDate()  &&  imp.IsSetDate()) {
            SetDate(imp.SetDate());
            imp.ResetDate();
        }
        if (!imp.IsSetPub()) {
            ResetImp();
        }
    }
    if (authors  &&  authors->IsSetAffil()) {
        //!!! TO DO
    }
}


