void CPub_equiv::x_FlattenPubEquiv(void)
{
	Tdata& data = Set();

	Tdata::iterator it = data.begin();
	while(it != data.end()) {
		if ((*it)->IsEquiv()) {
			CPub_equiv& equiv = (*it)->SetEquiv();
			equiv.x_FlattenPubEquiv();
			copy(equiv.Set().begin(), equiv.Set().end(), back_inserter(data));
			it = data.erase(it);
		} else {
			++it;
		}
	}
}

