CValidErrItem::CValidErrItem(
    EDiagSev             sev,
    unsigned int         ec,
    const string&        msg,
    const string&        desc,
    const CSerialObject* obj,
    const CSeq_entry*    ctx,
    const string&        acc,
    const int            ver,
    const int            seq_offset)
{
    SetSev(sev);
    SetErrIndex(ec);
    SetMsg(msg);
    SetObjDesc(desc);
    SetAccession(acc);
    SetSeqOffset(seq_offset);
    if (ver > 0) {
        SetAccnver(acc + "." + NStr::IntToString(ver));
    } else {
        SetAccnver(acc);
    }
    SetVersion(ver);
    SetErrorName(ConvertErrCode(ec));
    SetErrorGroup(ConvertErrGroup(ec));
    if (obj) {
        SetObject(*obj);
    }
    if (ctx) {
        m_Ctx.Reset(ctx);
    }
}


void CValidErrItem::SetFeatureObjDescFromFields()
{
    // Add feature part of label
    string desc = "FEATURE: ";
    if (IsSetObj_content()) {
        desc += GetObj_content();
    }

    // Add feature ID part of label (if present)
    if (IsSetFeatureId()) {
        desc += " <" + GetFeatureId() + "> ";
    }

    // Add feature location part of label
    if (IsSetLocation()) {
        desc += " [" + GetLocation() + "]";
    }

    // Append label for bioseq of feature location
    if (IsSetBioseq()) {
        desc += GetBioseq();
    }

    // Append label for product of feature
    if (IsSetProduct_loc()) {
        desc += " -> " + GetProduct_loc();
    }

    SetObjDesc(desc);
}


