void CInt_fuzz::GetLabel(string* label, TSeqPos pos, bool right) const
{
    char lim = 0;
    switch (Which()) {
    case CInt_fuzz::e_P_m:
        (*label) += "<+-" + NStr::IntToString(GetP_m()) + ">";
        break;
    case CInt_fuzz::e_Range:
        (*label) += "<" + NStr::IntToString(GetRange().GetMin()) +
            "." + NStr::IntToString(GetRange().GetMax()) + ">";
        break;
    case CInt_fuzz::e_Pct:
        (*label) += "<" + NStr::IntToString(GetPct()) + "%>";
        break;
    case CInt_fuzz::e_Lim:
        switch (GetLim()) {
        case eLim_unk:
        case eLim_other:
            (*label) += "<?>";
            break;
        case eLim_gt:
            lim = '>';
            break;
        case eLim_lt:
            lim = '<';
            break;
        case eLim_tr:
            lim = 'r';
            break;
        case eLim_tl:
            lim = '^';
            break;
        default:
            break;
        }
        break;
    default:
        break;
    }
    
    if (lim  &&  lim != 'r') {
        (*label) += lim;
        lim = 0;
    }
    
    if (right) {
        (*label) += NStr::IntToString(pos);
    }
    
    if (lim == 'r') {
        (*label) += '^';
    }
    
    if (!right) {
        (*label) += NStr::IntToString(pos);
    }
}


