ostream& CSeq_id::AsFastaString(ostream& s) const
{
	E_Choice the_type = Which();
	if (the_type > e_Pdb)  // new SeqId type
		the_type = e_not_set;

	s << txtid[the_type] << '|';

	switch (the_type)
	{
           case e_not_set:
		break;
           case e_Local:
		GetLocal().AsString(s);
		break;
           case e_Gibbsq:
		s << GetGibbsq();
		break;
           case e_Gibbmt:
		s << GetGibbmt();
		break;
           case e_Giim:
		s << (GetGiim().GetId());
		break;
           case e_Genbank:
		GetGenbank().AsFastaString(s);
		break;
           case e_Embl:
		GetEmbl().AsFastaString(s);
		break;
           case e_Pir:
		GetPir().AsFastaString(s);
		break;
           case e_Swissprot:
		GetSwissprot().AsFastaString(s);
		break;
           case e_Patent:
		GetPatent().AsFastaString(s);
		break;
           case e_Other:
		GetOther().AsFastaString(s);
		break;
           case e_General:
		{
		const CDbtag& dbt = GetGeneral();
		s << Upcase(dbt.GetDb()) << '|' << (dbt.GetTag().AsString(s));
		}
		break;
           case e_Gi:
		s << GetGi();
		break;
           case e_Ddbj:
		GetDdbj().AsFastaString(s);
		break;
           case e_Prf:
		GetPrf().AsFastaString(s);
		break;
           case e_Pdb:
		GetPdb().AsFastaString(s);
		break;
	   default:
		s << "[UnknownSeqIdType]";
		break;

	}
	return s;
}
