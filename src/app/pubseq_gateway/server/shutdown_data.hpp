#include <chrono>
#include <atomic>
using namespace std;


struct SShutdownData
{
    atomic_int_fast64_t                         m_ActiveRequestCount;
    atomic_bool                                 m_ShutdownRequested;
    chrono::time_point<chrono::steady_clock>    m_Expired;

    SShutdownData() :
        m_ActiveRequestCount(0),
        m_ShutdownRequested(false)
    {}
};


